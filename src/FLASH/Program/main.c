void testFlash( void )
{
  LED_R_Set();
  LED_G_Set();
  LED_B_Set();

#if FLASH_RW_U8
  FLASH_EraseSectors(WRITE_SECTOR, 1);
  FLASH_WritePageU8(FLASH_USER_ADDR, WriteDataU8, 1024);
  FLASH_ReadPageU8(FLASH_USER_ADDR, ReadDataU8, 1024);
  if (compareArray(KS_U8, WriteDataU8, ReadDataU8, 1024) == KS_OK) {
    LED_R_Reset();
  }
  delay_ms(500);
#endif

#if FLASH_RW_U16
  FLASH_EraseSectors(WRITE_SECTOR, 1);
  FLASH_WritePageU16(FLASH_USER_ADDR, WriteDataU16, 512);
  FLASH_ReadPageU16(FLASH_USER_ADDR, ReadDataU16, 512);
  if (compareArray(KS_U16, WriteDataU16, ReadDataU16, 512) == KS_OK) {
    LED_G_Reset();
  }
  delay_ms(500);
#endif

#if FLASH_RW_U32
  FLASH_EraseSectors(WRITE_SECTOR, 1);
  FLASH_WritePageU32(FLASH_USER_ADDR, WriteDataU32, 256);
  FLASH_ReadPageU32(FLASH_USER_ADDR, ReadDataU32, 256);
  if (compareArray(KS_U32, WriteDataU32, ReadDataU32, 256) == KS_OK) {
    LED_B_Reset();
  }
  delay_ms(500);
#endif

#if FLASH_RW_U64
  FLASH_EraseSectors(WRITE_SECTOR, 1);
  FLASH_WritePageU64(FLASH_USER_ADDR, WriteDataU64, 128);
  FLASH_ReadPageU64(FLASH_USER_ADDR, ReadDataU64, 128);
  if (compareArray(KS_U64, WriteDataU64, ReadDataU64, 128) == KS_OK) {
    LED_R_Toggle();
    delay_ms(100);
    LED_R_Toggle();
    delay_ms(100);
  }
  delay_ms(500);
#endif

  delay_ms(1000);
}

int main( void )
{
  HAL_Init();
  BSP_GPIO_Config();

  testFlash();

  while (1) {
    LED_R_Toggle();
    LED_G_Toggle();
    LED_B_Toggle();
    delay_ms(200);
  }
}

